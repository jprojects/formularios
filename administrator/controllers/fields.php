// No direct access.
defined('_JEXEC') or die;

jimport('joomla.application.component.controlleradmin');

use Joomla\Utilities\ArrayHelper;

/**
 * Forms list controller class.
 *
 * @since  1.6
 */
class FormulariosControllerFields extends JControllerAdmin
{
	/**
	 * Method to clone existing Forms
	 *
	 * @return void
	 */
	public function duplicate()
	{
		// Check for request forgeries
		Jsession::checkToken() or jexit(JText::_('JINVALID_TOKEN'));
		
		$formid = JFactory::getApplication()->input->get('formId');

		// Get id(s)
		$pks = $this->input->post->get('cid', array(), 'array');

		try
		{
			if (empty($pks))
			{
				throw new Exception(JText::_('COM_FORMULARIOS_NO_ELEMENT_SELECTED'));
			}

			ArrayHelper::toInteger($pks);
			$model = $this->getModel();
			$model->duplicate($pks);
			$this->setMessage(Jtext::_('COM_FORMULARIOS_ITEMS_SUCCESS_DUPLICATED'));
		}
		catch (Exception $e)
		{
			JFactory::getApplication()->enqueueMessage($e->getMessage(), 'warning');
		}

		$this->setRedirect('index.php?option=com_formularios&view=fields&formId='.$formid);
	}
	
	function eliminar()
	{
		$jinput = JFactory::getApplication()->input;
		$id 	= $jinput->get('id');
		$formid = $jinput->get('formId');
		
		$db = JFactory::getDbo();
		
		$db->setQuery('DELETE FROM #__formularios_fields WHERE id = '.$id);
		
		if($db->query()) {
			$msg  = 'El campo se ha eliminado';
			$type = 'info';
		} else {
			$msg  = 'El campo no se pudo eliminar';
			$type = 'error';
		}
		
		$this->setRedirect('index.php?option=com_formularios&view=fields&formId='.$formid, $msg, $type);
	}

	/**
	 * Proxy for getModel.
	 *
	 * @param   string  $name    Optional. Model name
	 * @param   string  $prefix  Optional. Class prefix
	 * @param   array   $config  Optional. Configuration array for model
	 *
	 * @return  object	The Model
	 *
	 * @since    1.6
	 */
	public function getModel($name = 'field', $prefix = 'FormulariosModel', $config = array())
	{
		$model = parent::getModel($name, $prefix, array('ignore_request' => true));

		return $model;
	}

	/**
	 * Method to save the submitted ordering values for records via AJAX.
	 *
	 * @return  void
	 *
	 * @since   3.0
	 */
	public function saveOrderAjax()
	{
		// Get the input
		$input = JFactory::getApplication()->input;
		$pks   = $input->post->get('cid', array(), 'array');
		$order = $input->post->get('order', array(), 'array');

		// Sanitize the input
		ArrayHelper::toInteger($pks);
		ArrayHelper::toInteger($order);

		// Get the model
		$model = $this->getModel();

		// Save the ordering
		$return = $model->saveorder($pks, $order);

		if ($return)
		{
			echo "1";
		}

		// Close the application
		JFactory::getApplication()->close();
	}
}
